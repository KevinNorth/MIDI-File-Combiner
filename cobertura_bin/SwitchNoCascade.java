public class SwitchNoCascade {
	public static void main(String[] args) {
		int x = 4;
		switch(x){
		
			case 2:
				int d = 7 - 3;
				break;
			case 4:
				int a = 1 + 2;
				break;
			case 1:
				int b = 3 + x;
				break;
			case 23:
				int c = 5;
				break;
			default:
				int e = 4;
				int f = 6;
				
		}
		
		
		}
	}